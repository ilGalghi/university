public class Vettore3 {

	public static void main(String[] args) {
		String[] s;				//dichiaro stringa
		s = new String[3];		//inizializzo Vettore stringa con dimensione 3
		
		s[0] = "abcd";			//in posizione 0 dell'array inserisco "abcd"
		s[1] = "efgh";
		System.out.println(s[0]);	//stampa valore s[0], ovvero "abcd"
		System.out.println(s[1]);	//stampa valore s[1], ovvero "efgh"
		
		
	}

}
